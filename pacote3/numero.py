
class Numero:
    def __init__(self, x, y):
        self.x = x
        self.y = y
    
    def soma(self):
        return self.x + self.y
    
    def subtracao(self):
        return self.x - self.y
    
    def prod(self):
        return self.x * self.y
    
    def pont(self):
        return self.x ** self.y

        